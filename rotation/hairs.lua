function hairs.wallkick(piece, a, b)
  if a == 0 then
    a = 4
  end
  if b == 0 then
    b = 4
  end
  if a == 5 then
    a = 1
  end
  if b == 5 then
    b = 1
  end
  local ax, ay = 0
  local offset = JLSZT
  if gamestate.piece.name == "I" then
    offset = I
  end
  if gamestate.piece.name == "O" then
    offset = O
  end
  local kicks = offset[names[a] .. names[b]]
  for _, kick in ipairs(kicks or {{0,0}}) do
    ax = gamestate.x + kick[1]
    ay = gamestate.y + (kick[2] * -1)
    if not gamestate.board:isColliding(hairs.structure[gamestate.piece.name][b], ax, ay) then
      return false, kick[1], kick[2] * -1
    end
  end
  return true, 0, 0
end

function hairs:getSpawnLocation()
  return math.floor(FIELD_WIDTH/2)-2, HIDDEN_HEIGHT-2
end

